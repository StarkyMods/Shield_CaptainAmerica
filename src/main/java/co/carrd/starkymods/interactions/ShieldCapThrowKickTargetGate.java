package co.carrd.starkymods.interactions;

import javax.annotation.Nonnull;

import com.hypixel.hytale.codec.builder.BuilderCodec;
import com.hypixel.hytale.protocol.InteractionState;
import com.hypixel.hytale.protocol.InteractionType;
import com.hypixel.hytale.protocol.WaitForDataFrom;
import com.hypixel.hytale.server.core.entity.InteractionContext;
import com.hypixel.hytale.server.core.modules.interaction.interaction.CooldownHandler;
import com.hypixel.hytale.server.core.modules.interaction.interaction.config.SimpleInstantInteraction;

public final class ShieldCapThrowKickTargetGate extends SimpleInstantInteraction {
    @Nonnull
    public static final BuilderCodec<ShieldCapThrowKickTargetGate> CODEC =
            BuilderCodec
                    .builder(
                            ShieldCapThrowKickTargetGate.class,
                            ShieldCapThrowKickTargetGate::new,
                            SimpleInstantInteraction.CODEC
                    )
                    .documentation("Allows throw-kick damage only once per target until the projectile starts returning.")
                    .build();

    @Nonnull
    @Override
    public WaitForDataFrom getWaitForDataFrom() {
        return WaitForDataFrom.Server;
    }

    @Override
    protected void firstRun(@Nonnull InteractionType type,
                            @Nonnull InteractionContext context,
                            @Nonnull CooldownHandler cooldownHandler) {
        if (!ShieldCapThrowHomingService.shouldApplyThrowKickDamageToTarget(context)) {
            markFailed(context);
        }
    }

    private static void markFailed(@Nonnull InteractionContext context) {
        if (context.getState() != null) {
            context.getState().state = InteractionState.Failed;
        }
        if (context.getClientState() != null) {
            context.getClientState().state = InteractionState.Failed;
        }
        if (context.getServerState() != null) {
            context.getServerState().state = InteractionState.Failed;
        }
    }
}
